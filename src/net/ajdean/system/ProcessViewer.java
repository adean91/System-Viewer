    private void setDetailInfo(int process) {
        if (process > -1) {
            SystemProcess sp = processes.get(process);
            txtImageName.setText(sp.getImageName());
            txtProcessID.setText(sp.getProccessID());
            txtSessionName.setText(sp.getSessionName());
            txtSessionID.setText(sp.getSessionID());
            txtMemory.setText(sp.getMemory());
            txtStatus.setText(sp.getStatus());
            txtUserName.setText(sp.getUserName());
            txtCpuTime.setText(sp.getCpuTime());
            txtWinTitle.setText(sp.getWindowTitle());
        } else {
            txtImageName.setText("");
            txtProcessID.setText("");
            txtSessionName.setText("");
            txtSessionID.setText("");
            txtMemory.setText("");
            txtStatus.setText("");
            txtUserName.setText("");
            txtCpuTime.setText("");
            txtWinTitle.setText("");
        }
    }
    
