    /**
     * (Re)sets the processes list.
     */
    private void setProcessList() {
        System.out.println("Refresh");
        processes.clear();
        listModel.removeAllElements();
        
        try {
            processes = fetchProcesses();
            for (int i = 0; i < processes.size(); i++) {
                listModel.addElement(processes.get(i).getImageName());
            } 
        } catch (Exception ex) {
            Logger.getLogger(ProcessViewer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    /**
     * Gets the current system processes.
     * 
     * @return ArrayList
     * @throws IOException
     * @throws Exception
     */
    public ArrayList<SystemProcess> fetchProcesses() throws IOException, Exception {      
        ArrayList proc = new ArrayList();  
        
        Process cmd = new ProcessBuilder("tasklist.exe", "/fo", "csv", "/nh", "/v").start();

        new Thread(() -> {
            Scanner sc = new Scanner(cmd.getInputStream());
            if (sc.hasNextLine()) sc.nextLine();
            while (sc.hasNextLine()) {
                String line = sc.nextLine();
                String[] parts = line.split("\"*\",");
                for (int i = 0; i < parts.length; i++) { parts[i] = parts[i].replace("\"", ""); }
                
                proc.add(new SystemProcess(parts));
                
                //System.out.println(i + ": " + names[i] + " = " + parts[i]);
            }
        }).start();
        cmd.waitFor(); 
        
        return proc;
    }    
    

